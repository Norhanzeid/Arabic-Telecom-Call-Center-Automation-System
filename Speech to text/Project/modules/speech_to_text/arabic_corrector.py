# GPT-Based Correction (More Accurate)
# =============================================================================
def correct_arabic_text_gpt(text: str) -> str:
    """
    Corrects Arabic text using OpenAI GPT.
    More accurate than dictionary-based correction but costs ~$0.002/call.
    
    Args:
        text: Arabic text to correct (from Whisper transcription)
        
    Returns:
        str: Corrected Arabic text
    """
    if not text or not text.strip():
        return text
    
    print("🤖 Correcting Arabic text with GPT...")
    
    try:
        client = get_openai_client()
        response = client.chat.completions.create(
            model="gpt-3.5-turbo",
            messages=[
                {
                    "role": "system",
                    "content": load_prompt("arabic_corrector_system")
                },
                {
                    "role": "user",
                    "content": f"صحح النص التالي مع الحفاظ على اللهجة المصرية:\n\n{text}"
                }
            ],
            temperature=0.1,
            max_tokens=2000
        )
        
        corrected = response.choices[0].message.content.strip()
        print("✅ GPT correction completed!")
        return corrected
        
    except Exception as e:
        print(f"⚠️ GPT correction failed: {e}")
        print("↩️ Falling back to dictionary correction...")
        return correct_arabic_text(text)

