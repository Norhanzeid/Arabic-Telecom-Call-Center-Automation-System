def normalize_arabic_chars(text: str) -> str:
    """
    Normalizes Arabic characters for consistency.
    
    Args:
        text: Arabic text to normalize
        
    Returns:
        str: Normalized text
    """
    # Normalize Alef variations (but keep hamza when meaningful)
    text = re.sub(r'آ', 'ا', text)  # Alef with madda
    text = re.sub(r'إ', 'ا', text)  # Alef with hamza below
    text = re.sub(r'أ', 'ا', text)  # Alef with hamza above
    
    # Normalize Yaa
    text = re.sub(r'ى', 'ي', text)  # Alef maksura to yaa
    
    # Remove Tatweel (kashida)
    text = re.sub(r'ـ+', '', text)
    
    # Remove diacritics (tashkeel)
    arabic_diacritics = re.compile(r'[\u064B-\u065F\u0670]')
    text = arabic_diacritics.sub('', text)
    
    return text


def fix_spacing_and_punctuation(text: str) -> str:
    """
    Fixes spacing and punctuation issues in Arabic text.
    
    Args:
        text: Arabic text to process
        
    Returns:
        str: Text with fixed spacing and punctuation
    """
    # Ensure proper spacing around punctuation
    text = re.sub(r'\s*([.,،])\s*', r'\1 ', text)
    text = re.sub(r'\s+', ' ', text)  # Normalize spaces

    # Add a period at the end if missing
    if not text.endswith("."):
        text += "."

    return text.strip()



