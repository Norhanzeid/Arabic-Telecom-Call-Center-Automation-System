def clean_number_dashes(text: str) -> str:
    """
    Removes dashes and hyphens between digits in the text.
    Converts patterns like '0-551-234-567' to '0551234567'.
    
    Args:
        text: Text that may contain numbers with dashes
        
    Returns:
        str: Text with clean numbers (no dashes between digits)
    """
    # Remove dashes/hyphens that appear between digits: 0-551-234-567 -> 0551234567
    return re.sub(r'(?<=\d)[\s-]+(?=\d)', '', text)


